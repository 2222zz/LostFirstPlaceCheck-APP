package com.example.lfpapp;

public class ScoreData {
    String player;
    int x320;
    int x300;
    int x200;
    int x100;
    int x50;
    int x0;

    public ScoreData() {
        this.player = "";
        this.x320 = 0;
        this.x300 = 0;
        this.x200 = 0;
        this.x100 = 0;
        this.x50 = 0;
        this.x0 = 0;
    }

    public String getPlayer() {
        return player;
    }

    public void setPlayer(String player) {
        this.player = player;
    }

    public int getX320() {
        return x320;
    }

    public void setX320(int x320) {
        this.x320 = x320;
    }

    public int getX300() {
        return x300;
    }

    public void setX300(int x300) {
        this.x300 = x300;
    }

    public int getX200() {
        return x200;
    }

    public void setX200(int x200) {
        this.x200 = x200;
    }

    public int getX100() {
        return x100;
    }

    public void setX100(int x100) {
        this.x100 = x100;
    }

    public int getX50() {
        return x50;
    }

    public void setX50(int x50) {
        this.x50 = x50;
    }

    public int getX0() {
        return x0;
    }

    public void setX0(int x0) {
        this.x0 = x0;
    }
}
